"use client";
import { cn } from "./utils";
import { IconMenu2, IconX } from "@tabler/icons-react";
import {
  motion,
  AnimatePresence,
  useScroll,
  useMotionValueEvent,
} from "motion/react";

import React, { useRef, useState } from "react";


export const Navbar = ({
  children,
  className,
}) => {
  const ref = useRef(null);
  const { scrollY } = useScroll({
    target: ref,
    offset: ["start start", "end start"],
  });
  const [visible, setVisible] = useState(false);

  useMotionValueEvent(scrollY, "change", (latest) => {
    setVisible(latest > 100);
  });

  return (
    <motion.div
      ref={ref}
      className={cn(
        "sticky inset-x-0 top-20 z-40 w-full px-4 py-2",
        "transition-all duration-300 ease-in-out",
        visible ? "bg-white/80 dark:bg-neutral-950/10" : "bg-transparent",
        "",
        className
      )}
    >
      
      {React.Children.map(children, (child) =>
        React.isValidElement(child)
          ? React.cloneElement(child, { visible })
          : child
      )}
    
    </motion.div>
  );
};


export const NavBody = ({
  children,
  className,
  visible
}) => {
  return (
    <motion.div
      animate={{
        backdropFilter: visible ? "blur(10px)" : "none",
        width: visible ? "50%" : "100%",
        y: visible ? 20 : 0,
      }}
      transition={{
        type: "spring",
        stiffness: 200,
        damping: 50,
      }}
      style={{
        maxWidth: "800px",
      }}
      className={cn(
        "relative z-[60] mx-auto hidden w-full flex-row items-center justify-between bg-transparent px-4 py-4 lg:flex dark:bg-transparent",
        visible && "",
        className
      )}>
      {children}
    </motion.div>
  );
};




